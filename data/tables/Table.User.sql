CREATE TABLE User (
    Id        INTEGER       PRIMARY KEY ON CONFLICT FAIL AUTOINCREMENT,
    FirstName VARCHAR (100) NOT NULL ON CONFLICT FAIL,
    LastName  VARCHAR (100) NOT NULL ON CONFLICT FAIL,
    UserName  VARCHAR (50)  NOT NULL ON CONFLICT FAIL
                            UNIQUE ON CONFLICT FAIL,
    Password  VARCHAR (10)  NOT NULL ON CONFLICT FAIL,
    Salt      VARCHAR (50)  NOT NULL ON CONFLICT FAIL
);
